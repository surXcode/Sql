1.WAQTD NAME OF THE EMPLOYEE AND HIS MANAGER'S
NAME IF EMPLOYEE IS WORKING AS CLERK.

1.SELECT E1.ENAME,E2.ENAME
FROM EMP E1,EMP E2
WHERE E1.MGR=E2.EMPNO AND E1.JOB='CLERK';

2.WAQTD NAME OF THE EMPLOYEE AND MANAGER'S
DESIGNATION IF MANAGER WORKS IN DEPT 10 OR 20.

2. SELECT E1.ENAME,E2.JOB
 FROM EMP E1,EMP E2
 WHERE E1.MGR=E2.EMPNO AND E2.DEPTNO IN (10,20);

3..WAQTD NAME OF THE EMP AND MANAGERS SALARY IF
EMPLOYEE AND MANAGER BOTH EARN MORE THAN 2300.

3.SELECT E1.ENAME,E2.SAL
FROM EMP E1,EMP E2
WHERE E1.MGR=E2.EMPNO AND E1.SAL >2300 AND E2.SAL >2300;

4.WAQTD EMP NAME AND MANAGER'S HIREDATE IF
EMPLOYEE WAS HIRED BEFORE1982.

4.SELECT E1.ENAME,E2.HIREDATE
 FROM EMP E1,EMP E2
WHERE E1.MGR=E2.EMPNO AND E1.HIREDATE < '01-JAN-82';

5.WAQTD EMP NAME AND MANAGER'S COMM IF
EMPLOYEE WORKS AS SALESMAN AND MANAGER
WORKS IN DEPT 30 .

5.SELECT E1.ENAME,E2.COMM
FROM EMP E1,EMP E2
WHERE E1.MGR=E2.EMPNO AND E1.JOB='SALESMAN' AND E2.DEPTNO=30;

6.WAQTD EMP NAME AND MANAGER NAME AND THEIR
SALARIES IF EMPLOYEE EARNS MORE THAN MANAGER.

6.SELECT E1.ENAME,E1.SAL,E2.ENAME,E2.SAL
FROM EMP E1,EMP E2
WHERE E1.MGR=E2.EMPNO AND E1.SAL>E2.SAL;

